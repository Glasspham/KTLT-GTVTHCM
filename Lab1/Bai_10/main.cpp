#include<iostream>
#include<math.h>
using namespace std;
int main()
{
    double a;
    cout << "Chieu dai 1 canh hinh vuong: "; cin >> a;
    cout << "Chu vi hinh vuong: " << 2 * a << endl;
    cout << "Dien tich hinh vuong: " << a * a << endl;
    cout << "Do dai duong cheo hinh vuong: " << sqrt(2) * a << endl;
}